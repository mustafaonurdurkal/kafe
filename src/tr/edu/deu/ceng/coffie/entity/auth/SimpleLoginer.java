package tr.edu.deu.ceng.coffie.entity.auth;

public class SimpleLoginer implements Loginnable{

	private String email,password;

	
	
	public SimpleLoginer(String email, String password) {
		super();
		this.email = email;
		this.password = password;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}
	
	

}
